#python usa typado dinamico
#int or Long
numero = 10
#con coma se puede concatenar sin transformar a string
print(numero,type(numero))
#loat
numero_F = 3.2
print(numero_F,type(numero_F))
#complex
numero_C = 19 + 4j
print(numero_C,type(numero_C))
#string
nombre = "Sergio"
print(nombre,type(nombre))
#bool
verdadero_falso = 7 == 7
print(verdadero_falso,type(verdadero_falso))